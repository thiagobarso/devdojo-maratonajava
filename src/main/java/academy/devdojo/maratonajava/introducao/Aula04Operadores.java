public class Aula04Operadores {
    public static void main(String[] args) {
        // + - / *
        int numeros01 = 10;
        int numeros02 = 20;
        System.out.println(numeros02 + numeros01 + " Valor " + numeros02 + numeros01);
        double resultado = (double) numeros01 / numeros02;
        System.out.println(resultado);
    }
}
